<?php 
    //While
    $compteur = 6;
    while($compteur <= 5){
        echo "{While} Le compteur est : $compteur \n";
        $compteur++;
    }

    echo "\n\n";
    //Do while
    $compteur = 6;
    do{
        echo "{Do_ while} Le compteur est : $compteur \n";
        $compteur++;
    }while($compteur <= 5);
?>